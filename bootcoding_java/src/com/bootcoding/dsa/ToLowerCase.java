package com.bootcoding.dsa;
//709. To Lower Case
public class ToLowerCase {
    public static void main(String[] args) {
        String s = "Hello";
    //    Output: "hello"
        System.out.println(toLowerCase(s));
    }
    public static String toLowerCase(String s) {

        String str =(s.toLowerCase());
        return str;
    }
}
