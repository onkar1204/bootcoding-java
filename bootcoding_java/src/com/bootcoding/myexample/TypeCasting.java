package com.bootcoding.myexample;

public class TypeCasting {
    public static void main(String[] args) {
        int i = 10;
        int s = i++;
        System.out.println(s);
        int x = 10;
        int res = 10 + x++;
        System.out.println(res);
        int pqr = ++x + 10;
        System.out.println(pqr);
    }

}
