public class array1 {
    public static void main(String[] args) {
        int age[] = new int [5];
        age[0] = 10;
        age[1] = 15;
        age[2] = 20;
        age[3] = 25;
        age[4] = 30;
        for(int count=0;  count < 5; count++){
            System.out.println(age[count]);
        }

    }

}
